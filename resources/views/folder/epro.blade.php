<div class="div3"><b style="font-size:20px;">Grade Point Average</b>
<hr/>
<p>Grade Point Average (GPA) is the credit-weighted arithmetic mean of the Grade Point Values, which is determined by dividing the total credit-weighted Grade Point Value by the total number of credits. GPA shall be computed to the second decimal place.
<br>Example: A student who has completed one course unit with two credits, three course units each of three credits and two course units each of 1 credit with grades A, C, B, D, C+ and A+ respectively would have the GPA of 2.48 as calculated below</p>
