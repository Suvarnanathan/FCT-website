<p style="margin-left:-100px;font-size:30px;margin-top:50px;margin-right:-100px;"><b>THE 2ND KELANIYA INTERNATIONAL CONFERENCE ON ADVANCES IN COMPUTING AND </b><p style="margin-left:400px;font-size:30px;"><b>TECHNOLOGY (KICACT) – 2017</b></p>
<hr style="margin-left:300px;margin-right:500px; background-color:red"/>
<div class="div2">
<p style="text-align:justify;">The Second Kelaniya International Conference on Advances in Computing and Technology – (KICACT) – 2017, organized by the Faculty of Computing and Technology (FCT), University of Kelaniya was successfully held on the 28th of July 2017, at the Auditorium of the Faculty of Commerce and Financial Management Studies. The theme of the conference was“Contributing Computing and Technology to achieve Sustainable Development Goals”. The Chief Guest of the conference was Prof. Lakshman Senevirathne, the Deputy Vice Chancellor of the University of Kelaniya.<br>
The keynote speech on “Sustainability through Cloud Computing” was delivered by Prof. Subarna Shakya of Department of Electronics and Computer Engineering, Tribhuvan University, Nepal. Technical sessions consisted of 44 international and local oral presentations and 17 poster presentations on following themes; Industrial Technology, Intelligent system, Theoretical computing, Material technology, Applied computing and E-learning technology. In addition, there was a tech talk on “Ensure to affordable, reliable, sustainable and modern technology for all” by Eng. (Mr.) Ananda Devasinghe, the president of the Private sector Engineers’ Society, Sri Lanka<br>
Cadre Chair & Senior Prof. N.P. Sunil Chandra, the Chairman of the Research Council, Dr. Gamini Wijayarathna, Dean of Faculty of Computing and Technology, Dr. Asanka Pallewatta of the FCT, the Conference Chair, academic and administrative staff members of the university, national and international scholars, industrial experts and the undergraduates participated in this conference</p>
