<p style="margin-left:-100px;font-size:30px;margin-top:50px;margin-right:-100px;"><b>THE 1ST KELANIYA INTERNATIONAL CONFERENCE ON ADVANCES IN COMPUTING AND </b><p style="margin-left:400px;font-size:30px;"><b>TECHNOLOGY (KICACT) – 2016</b></p>
<hr style="margin-left:300px;margin-right:500px; background-color:red"/>
<div class="div2">
<p style="text-align:justify;">
The first Kelaniya International Conference on Advances in Computing and Technology, organized by the Faculty of Computing and Technology, University of Kelaniya was held on 25th November 2016, at Hilton Colombo Residencies. Senior Professor Sunanda Madduma Bandara, the Vice-Chancellor of the University of Kelaniya was the chief guest of this event.

The keynote speech of the conference was delivered by Professor Marcos A Rodrigues, Head of the Geometric Modelling and Pattern Recognition (GMPR) research group, Sheffield Hallam University, United Kingdom on research issues in Artificial Intelligence (AI) and Data Security.<br>

Technical sessions consisted of 30 international and local oral presentations and 12 poster presentations on following themes; Artificial Intelligence (AI), Human Computer Interactions and Technologies (HCI), Data Security and e-Commerce.<br>

Dr. Gamini Wijayarathna, Dean of Faculty of Computing and Technology, Professor Ariyarathna Jayamaha, Dean of Faculty of Graduate Studies, Professor D.M. Semasinghe, Dean of Faculty of Commerce and Management Studies, Professor Lakshman Senevirathne, Dean of Faculty of Humanities, Professor Kapila Seneviratne, Dean of Faculty of Science, Professor A.H.M.H. Abayarathna, Dean of Faculty of Social Sciences and the academic staff, national and international scholars, industrial experts and students participated at this event. Also there was a technical talk by Mr. Ranga Kariyapperuma from CISCO systems.<br>

Concurrently, a workshop on 3-D reconstruction and computer vision for industrial applications organized by Faculty of Computing and Technology was conducted by Professor Rodrigues in the evening. The workshop was attended by industry experts, staff of the Faculty of Computing and Technology and students<br>
