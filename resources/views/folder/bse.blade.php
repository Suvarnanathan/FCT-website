<p style="margin-left:90px;font-size:25px;"><b>BACHELOR OF SCIENCE HONOURS IN COMPUTER SCIENCE DEGREE</b></p>
<hr style="margin-left:300px;margin-right:500px; background-color:red"/>
<div class="o">
    <div class="o1">
        <p style="text-align:justify;">The Bachelor of Science Honours in Computer Science degree programme offered by the Faculty of Computing and Technology is a four-year programme aligned with the recommendations of the Standing Committee on Computing of the University Grants Commission (UGC), and the guidelines stipulated by the Association for Computing Machinery (ACM) and the Institute of Electrical and Electronic Engineers (IEEE). The student intake for this degree programme is 50 at present.

This programme provides students with solid knowledge on theoretical concepts in the domain of Computer Science. This degree programme also provide the necessary skills and knowledge required to design and implement complex software and hardware systems through supporting hands-on laboratory sessions.<br>

The Bachelor of Science Honours in Computer Science degree programme offered by the Faculty of Computing and Technology (FCT) has a strong focus on practical applications of Computer Science with the view of preparing highly marketable graduates for a wide variety of industrial applications expected by the ever growing high-end IT industry in Sri Lanka.<br>

Incorporation of specialized application areas such as Information Security, Artificial Intelligence, Data Science and Analytics, Database Management, Virtualization and Cloud Computing, Network Management, Wireless and Mobile Communications, Web and Internet Programming, Mobile Application Development, Data Structures and Algorithms, Automata, and Virtual Reality to the curriculum provides ample flexibility for students to specialize in their own areas of interest.<br>

Depending on the studentâ€™s interest, a student will be able to follow a standard four year Computer Science degree programme or a special degree from third year on specialization areas such as Cyber Security, Data Science, Artificial Intelligence and Scientific Computing.<br>

Students will get an opportunity to work in the industry for six months through the compulsory internship programme to be completed in the 4th year as partial requirement to obtain the degree. The medium of instructions and examinations is English.</p>
